  <h2>Le littoral varois en danger&nbsp;!</h2>
  <div class="Contenue">
      <p class="Texte">
        Le Var et plus particulièrement son littoral méditerranéen - plus de 430 km de côtes, 600 km de cours d’eau, 400&nbsp;000 hectares d’espaces boisés, 3&nbsp;000 espèces de plantes, 350 espèces d’oiseaux - est en danger.
      </p>
      <p class="Texte">
        Malheureusement, encore trop de collectivités participent à sa destruction, dénient ou exploitent abusivement l'environnement naturel par une surpopulation estivale et une promotion immobilière irresponsables.
      </p>
      <p class="Texte">
        Néanmoins, tout n'est pas perdu. Ces dernières années, les membres du gouvernement français et européen et de nombreuses associations se sont rassemblés pour combattre la destruction de la méditerranée.
      </p>
      <p class="Texte">
        Dans ce site, nous recensons toutes les actions prissent dans le département pour la protection de notre mer, et mettons à votre disposition un espace de discussion pour que vous puissiez partager vos opinions et vos idées à ce sujet.
      </p>
